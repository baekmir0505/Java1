package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		/*
		 이름 : OOO
		 출생년도 : OOOO년
		 키 : ooo.oocm
		 */
		String 이름1 = "백미르";
		int 출생년도1 = 2000;
		double 키1 = 180.12;
		
		System.out.println("이름 : "+이름1);
		System.out.println("출생년도 : "+출생년도1+"년");
		System.out.println("키 : "+키1+"cm");
	}
}
